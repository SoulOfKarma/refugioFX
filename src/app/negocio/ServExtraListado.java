package app.negocio;

/**
 *
 * @author Ricar
 */
public class ServExtraListado {
    
    private int idServExtra;
    private String nombreServExtra;
    private int precioServExtra;
    private String nomProv;

    /**
     * @return the idServExtra
     */
    public int getIdServExtra() {
        return idServExtra;
    }

    /**
     * @param idServExtra the idServExtra to set
     */
    public void setIdServExtra(int idServExtra) {
        this.idServExtra = idServExtra;
    }

    /**
     * @return the nombreServExtra
     */
    public String getNombreServExtra() {
        return nombreServExtra;
    }

    /**
     * @param nombreServExtra the nombreServExtra to set
     */
    public void setNombreServExtra(String nombreServExtra) {
        this.nombreServExtra = nombreServExtra;
    }

    /**
     * @return the precioServExtra
     */
    public int getPrecioServExtra() {
        return precioServExtra;
    }

    /**
     * @param precioServExtra the precioServExtra to set
     */
    public void setPrecioServExtra(int precioServExtra) {
        this.precioServExtra = precioServExtra;
    }

    /**
     * @return the nomProv
     */
    public String getNomProv() {
        return nomProv;
    }

    /**
     * @param nomProv the nomProv to set
     */
    public void setNomProv(String nomProv) {
        this.nomProv = nomProv;
    }
    
    
    
}
