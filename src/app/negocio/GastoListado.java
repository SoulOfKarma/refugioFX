package app.negocio;

/**
 *
 * @author Ricar
 */
public class GastoListado {
    
    private int idGasto;
    private String nombreGasto;
    private String nombreTipoGasto;

    /**
     * @return the idGasto
     */
    public int getIdGasto() {
        return idGasto;
    }

    /**
     * @param idGasto the idGasto to set
     */
    public void setIdGasto(int idGasto) {
        this.idGasto = idGasto;
    }

    /**
     * @return the nombreGasto
     */
    public String getNombreGasto() {
        return nombreGasto;
    }

    /**
     * @param nombreGasto the nombreGasto to set
     */
    public void setNombreGasto(String nombreGasto) {
        this.nombreGasto = nombreGasto;
    }

    /**
     * @return the nombreTipoGasto
     */
    public String getNombreTipoGasto() {
        return nombreTipoGasto;
    }

    /**
     * @param nombreTipoGasto the nombreTipoGasto to set
     */
    public void setNombreTipoGasto(String nombreTipoGasto) {
        this.nombreTipoGasto = nombreTipoGasto;
    }
    
    
    
}
