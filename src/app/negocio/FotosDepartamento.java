package app.negocio;

/**
 *
 * @author Ricar
 */
public class FotosDepartamento {
    
    private int idDep;
    private String nomFoto;
    private int idFoto;

    /**
     * @return the idDep
     */
    public int getIdDep() {
        return idDep;
    }

    /**
     * @param idDep the idDep to set
     */
    public void setIdDep(int idDep) {
        this.idDep = idDep;
    }

    /**
     * @return the nomFoto
     */
    public String getNomFoto() {
        return nomFoto;
    }

    /**
     * @param nomFoto the nomFoto to set
     */
    public void setNomFoto(String nomFoto) {
        this.nomFoto = nomFoto;
    }

    /**
     * @return the idFoto
     */
    public int getIdFoto() {
        return idFoto;
    }

    /**
     * @param idFoto the idFoto to set
     */
    public void setIdFoto(int idFoto) {
        this.idFoto = idFoto;
    }
    
    
    
}
